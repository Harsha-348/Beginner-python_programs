# Normal calculator to add, subtract, multiply, and divide two numbers

# Get user input:
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Perform operations:
add = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2 

# Print results
print(f"Sum: {add}")
print(f"Difference: {subtract}")
print(f"Product: {multiply}")
print(f"Quotient: {divide}")
